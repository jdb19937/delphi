/*
 * εἰκών.h — rete adversarium generativum (GAN) pro imaginibus
 *
 * Architectura: MLP-GAN cum tribus stratis FC.
 *   Γενέτης (Generator):     z -> γ_κρυ1 -> γ_κρυ2 -> εικ_διαστ (tanh)
 *   Κριτής (Discriminator):  εικ_διαστ -> κ_κρυ1 -> κ_κρυ2 -> 1 (sigmoid)
 *
 * Operationes per phantasma/computo.h (matvec, ger, κτλ.).
 */

#ifndef ΕΙΚΩΝ_H
#define ΕΙΚΩΝ_H

#include <stddef.h>

/* ================================================================
 * σύνθεσις — configuratio
 * ================================================================ */

typedef struct {
    int ζ_διαστ;      /* dimensio latentis (z_dim) */
    int γ_κρυ1;       /* γενέτης κρυπτὸν 1 */
    int γ_κρυ2;       /* γενέτης κρυπτὸν 2 */
    int εικ_πλάτος;   /* latitudo imaginis */
    int εικ_ὕψος;     /* altitudo imaginis */
    int κ_κρυ1;       /* κριτὴς κρυπτὸν 1 */
    int κ_κρυ2;       /* κριτὴς κρυπτὸν 2 */
} εικ_σύνθεσις_t;

/* εικ_διαστ = εικ_πλάτος * εικ_ὕψος (derivatur) */

/* ================================================================
 * σταθμά — δεῖκται εἰς ἐπίπεδον ταμιεῖον
 *
 * Διάταξις:
 *   γw1 (γ_κρυ1, ζ_διαστ)   γb1 (γ_κρυ1)
 *   γw2 (γ_κρυ2, γ_κρυ1)    γb2 (γ_κρυ2)
 *   γw3 (εικ_δ, γ_κρυ2)     γb3 (εικ_δ)
 *   κw1 (κ_κρυ1, εικ_δ)     κb1 (κ_κρυ1)
 *   κw2 (κ_κρυ2, κ_κρυ1)    κb2 (κ_κρυ2)
 *   κw3 (1, κ_κρυ2)          κb3 (1)
 * ================================================================ */

typedef struct {
    /* γενέτης */
    float *γw1, *γb1;
    float *γw2, *γb2;
    float *γw3, *γb3;
    /* κριτής */
    float *κw1, *κb1;
    float *κw2, *κb2;
    float *κw3, *κb3;
} εικ_σταθμά_t;

/* ================================================================
 * εικ_t — πλῆρες ὑπόδειγμα
 * ================================================================ */

typedef struct {
    εικ_σύνθεσις_t  σύνθεσις;
    εικ_σταθμά_t    σταθμά;
    float          *δεδομένα;
    size_t          μέγεθος;
    /* reusabiles buffers pro inferentia (GPU registrati) */
    float          *inf_h1;   /* max(γ_κρυ1, κ_κρυ1) */
    float          *inf_h2;   /* max(γ_κρυ2, κ_κρυ2) */
    float          *inf_out;  /* εικ_διαστ */
} εικ_t;

/* ================================================================
 * εικ_ἄσκησις_t — κατάστασις ἀσκήσεως
 * ================================================================ */

typedef struct {
    /* ἐνεργοποιήσεις γενέτου (μνημονευθεῖσαι) */
    float *γ_h1, *γ_a1;     /* στρῶμα 1: πρὸ/μετὰ LeakyReLU */
    float *γ_h2, *γ_a2;     /* στρῶμα 2 */
    float *γ_h3, *γ_out;    /* στρῶμα 3: πρὸ/μετὰ tanh */
    float *γ_z;              /* εἴσοδος z */

    /* ἐνεργοποιήσεις κριτοῦ */
    float *κ_h1, *κ_a1;     /* στρῶμα 1 */
    float *κ_h2, *κ_a2;     /* στρῶμα 2 */
    float  κ_h3, κ_out;     /* στρῶμα 3 (βαθμωτόν) */
    float *κ_in;             /* εἴσοδος εἰκόνος */

    /* κλίσεις */
    float       *κλ_δεδ;
    εικ_σταθμά_t κλ;

    /* Adam m */
    float       *μ_δεδ;
    εικ_σταθμά_t μ;

    /* Adam v */
    float       *ν_δεδ;
    εικ_σταθμά_t ν;

    /* προσωριναὶ κλίσεις ἐνεργοποιήσεων */
    float *δ_tmp;      /* μέγιστον μέγεθος στρώματος */
    float *κ_δεικ;     /* κλίσις κριτοῦ ὡς πρὸς εἴσοδον */

    int βῆμα_γ;        /* βῆμα Adam γενέτου */
    int βῆμα_κ;        /* βῆμα Adam κριτοῦ */
} εικ_ἄσκησις_t;

/* ================================================================
 * δίκτυον — συναρτήσεις δικτύου (δίκτυον.c)
 * ================================================================ */

/* GPU: registra omnes buffers semel. ἀ potest esse NULL (inferentia solum). */
void εικ_gpu_ἄρχε(εικ_t *ε, εικ_ἄσκησις_t *ἀ);

/* μέγεθος σταθμῶν (ἀριθμὸς δεκαδικῶν) */
size_t εικ_μέγεθος_σταθμῶν(const εικ_σύνθεσις_t *σ);

/* ἀρχικοποίησις δεικτῶν σταθμῶν εἰς δεδομένα */
float *εικ_σταθμά_ἄρχε_δείκτ(
    εικ_σταθμά_t *π, const εικ_σύνθεσις_t *σ, float *δεδ
);

/* κτίζει νέον ὑπόδειγμα μετὰ τυχαίων σταθμῶν (Xavier) */
int εικ_ἄρχε_τυχαίως(εικ_t *ε, const εικ_σύνθεσις_t *σ, unsigned int σπέρμα);

/* ἀναγιγνώσκει ὑπόδειγμα ἐκ πτυχῆς */
int εικ_ἀνάγνωθι(εικ_t *ε, const char *ὁδός);

/* σῴζει ὑπόδειγμα εἰς πτυχήν */
int εικ_σῶσον(const εικ_t *ε, const char *ὁδός);

/* ἐλευθεροῖ μνήμας */
void εικ_τέλει(εικ_t *ε);

/* γενέτης πρόσω — εἴσοδος z, ἔξοδος εἰς ε->γ_out (ἄνευ μνήμης) */
void εικ_γεν_πρόσω(εικ_t *ε, const float *z, float *ἔξοδος);

/* γενέτης πρόσω μετὰ μνήμης ἐνεργοποιήσεων */
void εικ_γεν_πρόσω_μνήμη(εικ_t *ε, εικ_ἄσκησις_t *ἀ, const float *z);

/* κριτὴς πρόσω — ἀποδίδει πιθανότητα [0,1] */
float εικ_κρι_πρόσω(εικ_t *ε, const float *εικόνα);

/* κριτὴς πρόσω μετὰ μνήμης */
float εικ_κρι_πρόσω_μνήμη(εικ_t *ε, εικ_ἄσκησις_t *ἀ, const float *εικόνα);

/* ================================================================
 * ἄσκησις — συναρτήσεις ἀσκήσεως (ἄσκησις.c)
 * ================================================================ */

/* ἐκχωρεῖ κλίσεις, Adam, ἐνεργοποιήσεις */
int  εικ_ἄσκησις_ἄρχε(εικ_ἄσκησις_t *ἀ, const εικ_σύνθεσις_t *σ);
void εικ_ἄσκησις_τέλει(εικ_ἄσκησις_t *ἀ);

/* μηδενίζει κλίσεις */
void εικ_κλ_μηδέν(εικ_ἄσκησις_t *ἀ, const εικ_σύνθεσις_t *σ);

/* ἀναδρομὴ κριτοῦ — σωρεύει κλίσεις εἰς ἀ->κλ, ἀποδίδει BCE loss */
float εικ_κρι_ἀναδρομή(εικ_t *ε, εικ_ἄσκησις_t *ἀ, float στόχος);

/* ἀναδρομὴ γενέτου — χρησιμοποιεῖ κ_δεικ ἀπὸ κρι_ἀναδρομή */
void εικ_γεν_ἀναδρομή(εικ_t *ε, εικ_ἄσκησις_t *ἀ);

/* βῆμα Adam πρὸς κριτήν μόνον */
void εικ_βῆμα_ἀδάμ_κρι(
    εικ_t *ε, εικ_ἄσκησις_t *ἀ,
    float lr, float β1, float β2, float ἐψ
);

/* βῆμα Adam πρὸς γενέτην μόνον */
void εικ_βῆμα_ἀδάμ_γεν(
    εικ_t *ε, εικ_ἄσκησις_t *ἀ,
    float lr, float β1, float β2, float ἐψ
);

#endif /* ΕΙΚΩΝ_H */
