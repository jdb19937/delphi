/* ================================================================
 * ἐξάγαγε — συλλογισμοὶ μεταμορφωτοῦ ὀμφαλοῦ
 * ================================================================ */

#define ΟΜΦ_ΣΗΜΕΙΑ_ΜΕΓ        256
#define ΟΜΦ_ΓΕΝΝΗΣΕΙΣ_ΜΕΓ     256
#define ΟΜΦ_ΘΕΡΜΟΤΗΣ_ΠΡΟΕΠΙΛ  0.8f
#define ΟΜΦ_TOPP_ΠΡΟΕΠΙΛ      0.9f

static unsigned long long ὀμφ_σπέρμα = 42;


static float ὀμφ_τυχαῖος(unsigned long long *ρ)
{
    /* xorshift64 (ὡς llama2.c) */
    *ρ ^= *ρ >> 12;
    *ρ ^= *ρ << 25;
    *ρ ^= *ρ >> 27;
    return (float)((*ρ * 0x2545F4914F6CDD1Dull) >> 40) / 16777216.0f;
}

typedef struct {
    float π;
    int δ;
}ὀμφ_πδ_t;

static int ὀμφ_πδ_σύγκρ(const void *α, const void *β)
{
    float δ = ((const ὀμφ_πδ_t *)β)->π - ((const ὀμφ_πδ_t *)α)->π;
    return (δ > 0) - (δ < 0);
}

static char *ὀμφ_ἐξάγαγε(const char *ἐρώτημα)
{
    if (ὀμφ_ἀνάγνωθι_ὑπόδ(ὀμφ_ὄνομα_τρέχον) < 0)
        return strdup("ὀμφαλός: ὑπόδειγμα ἀναγνῶναι οὐκ ἠδυνήθην");

    ομφ_t         *ομφ       = &ὀμφ_ὑπόδειγμα;
    ομφ_λεκτήρ_t  *λεκ = ὀμφ_λεκτήρ_τρέχων;
    ομφ_σύνθεσις_t *σ  = &ομφ->σύνθεσις;
    int Λξ = σ->λεξ_μέγεθος;

    /* ψηφιοποίησον ἐρώτημα */
    int ν_ἐρ    = (int)strlen(ἐρώτημα) + 4;
    int *σημ_ἐρ = malloc((size_t)ν_ἐρ * sizeof(int));
    if (!σημ_ἐρ)
        return strdup("μνήμη ἐλλείπει");
    int ν_σημ_ἐρ = 0;
    ομφ_λεκτήρ_τέμε(λεκ, ἐρώτημα, 1, 0, σημ_ἐρ, &ν_σημ_ἐρ);

    ομφ_κατάστ_ἀποκατάστησον(ομφ);

    /* κατεργασία σημείων ἐρωτήματος */
    float *λογιταί = NULL;
    for (int τ = 0; τ < ν_σημ_ἐρ && τ < σ->μῆκος_μέγ - 1; τ++)
        λογιταί = ομφ_δρόμος(ομφ, σημ_ἐρ[τ], τ);

    int θέσις = ν_σημ_ἐρ < σ->μῆκος_μέγ ? ν_σημ_ἐρ : σ->μῆκος_μέγ - 1;
    free(σημ_ἐρ);

    /* γέννησις */
    int σημ_γεν[ΟΜΦ_ΣΗΜΕΙΑ_ΜΕΓ];
    int ν_γεν = 0;
    unsigned long long ρ = ὀμφ_σπέρμα;
    float θερμ = ΟΜΦ_ΘΕΡΜΟΤΗΣ_ΠΡΟΕΠΙΛ;
    float topp = ΟΜΦ_TOPP_ΠΡΟΕΠΙΛ;

    ὀμφ_πδ_t *πδ = malloc((size_t)Λξ * sizeof(ὀμφ_πδ_t));
    if (!πδ)
        return strdup("μνήμη ἐλλείπει");

    for (int βμ = 0; βμ < ΟΜΦ_ΓΕΝΝΗΣΕΙΣ_ΜΕΓ && θέσις < σ->μῆκος_μέγ; βμ++) {
        if (!λογιταί)
            break;

        /* θερμότης + softmax */
        for (int i = 0; i < Λξ; i++)
            λογιταί[i] /= θερμ;
        float μέγ = λογιταί[0];
        for (int i = 1; i < Λξ; i++)
            if (λογιταί[i] > μέγ)
                μέγ = λογιταί[i];
        float ἄθρ = 0.0f;
        for (int i = 0; i < Λξ; i++) {
            λογιταί[i] = expf(λογιταί[i] - μέγ);
            ἄθρ += λογιταί[i];
        }
        for (int i = 0; i < Λξ; i++)
            λογιταί[i] /= ἄθρ;

        /* top-p: praefiltrum, sort, accumulatio */
        float ὅριον = (1.0f - topp) / (float)(Λξ - 1);
        int ν_π     = 0;
        for (int i = 0; i < Λξ; i++) {
            if (λογιταί[i] >= ὅριον) {
                πδ[ν_π].π = λογιταί[i];
                πδ[ν_π].δ = i;
                ν_π++;
            }
        }
        qsort(πδ, ν_π, sizeof(ὀμφ_πδ_t), ὀμφ_πδ_σύγκρ);

        /* κεῖρον: πίπτει ἐπὶ σωρευτικοῦ > topp */
        float σωρ = 0.0f;
        int τελ   = ν_π - 1;
        for (int i = 0; i < ν_π; i++) {
            σωρ += πδ[i].π;
            if (σωρ > topp) {
                τελ = i;
                break;
            }
        }

        /* δειγματοληψία ἐκ κεκομμένου */
        float τυχ = ὀμφ_τυχαῖος(&ρ) * σωρ;
        float σαθ = 0.0f;
        int ἑπόμ  = πδ[0].δ;
        for (int i = 0; i <= τελ; i++) {
            σαθ += πδ[i].π;
            if (τυχ < σαθ) {
                ἑπόμ = πδ[i].δ;
                break;
            }
        }

        if (ἑπόμ == 2)
            break; /* σημεῖον τέλους */
        σημ_γεν[ν_γεν++] = ἑπόμ;
        λογιταί = ομφ_δρόμος(ομφ, ἑπόμ, θέσις++);
    }

    free(πδ);

    /* ἀποκωδικοποίησις — συνένωσον πάντα σημεῖα εἰς ἓν ταμιεῖον */
    size_t ὠμ_μεγ = (size_t)ν_γεν * ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ + 1;
    char *ὠμόν    = malloc(ὠμ_μεγ);
    if (!ὠμόν)
        return strdup("μνήμη ἐλλείπει");
    size_t δρομεύς = 0;
    for (int i = 0; i < ν_γεν; i++) {
        const char *κ = ομφ_λεκτήρ_ἀπόδος(λεκ, σημ_γεν[i]);
        size_t κμ     = strlen(κ);
        if (δρομεύς + κμ < ὠμ_μεγ) {
            memcpy(ὠμόν + δρομεύς, κ, κμ);
            δρομεύς += κμ;
        }
    }
    ὠμόν[δρομεύς] = '\0';

    /* καθάρισον UTF-8:
        παράλειψον ἀκύρους καὶ μὴ ἐκτυπωσίμους χαρακτῆρας */
    char *ἔκβασις = malloc(ὠμ_μεγ);
    if (!ἔκβασις) {
        free(ὠμόν);
        return strdup("μνήμη ἐλλείπει");
    }
    utf8_mundus(ἔκβασις, ὠμ_μεγ, ὠμόν);
    free(ὠμόν);
    return ἔκβασις;
}

