static char ὀμφ_ὄνομα_τρέχον[256] = "προεπιλεγμένον";

static ομφ_t         ὀμφ_ὑπόδειγμα;
static ομφ_λεκτήρ_t *ὀμφ_λεκτήρ_τρέχων   = NULL;
static char          ὀμφ_ὄνομα_ὑποδ[256] = "";
static int           ὀμφ_ἀρχικοποιημένον = 0;

/* ================================================================
 * ἀρχικοποίησις ὑποδείγματος
 * ================================================================ */

void ὀμφαλός_θές_ὄνομα(const char *ὄνομα)
{
    snprintf(
        ὀμφ_ὄνομα_τρέχον, sizeof(ὀμφ_ὄνομα_τρέχον),
        "%s", ὄνομα
    );
}

static int ὀμφ_ἀνάγνωθι_ὑπόδ(const char *ὄνομα)
{
    if (strcmp(ὄνομα, ὀμφ_ὄνομα_ὑποδ) == 0 && ὀμφ_ἀρχικοποιημένον)
        return 0; /* ἤδη ἀρχικοποιημένον */

    if (ὀμφ_ἀρχικοποιημένον) {
        ομφ_τέλει(&ὀμφ_ὑπόδειγμα);
        if (ὀμφ_λεκτήρ_τρέχων) {
            ομφ_λεκτήρ_κατάλυσον(ὀμφ_λεκτήρ_τρέχων);
            ὀμφ_λεκτήρ_τρέχων = NULL;
        }
        ὀμφ_ἀρχικοποιημένον = 0;
    }

    char ὁδ_bin[512], ὁδ_tok[512];
    snprintf(ὁδ_bin, sizeof(ὁδ_bin), "%s/model.bin", ὄνομα);
    snprintf(ὁδ_tok, sizeof(ὁδ_tok), "%s/tokenizer.bin", ὄνομα);

    if (ομφ_ἀνάγνωθι(&ὀμφ_ὑπόδειγμα, ὁδ_bin) < 0) {
        fprintf(stderr, "ὀμφαλός: οὐκ ἠδυνήθην ἀναγνῶναι '%s'\n", ὁδ_bin);
        return -1;
    }

    int λξ = ὀμφ_ὑπόδειγμα.σύνθεσις.λεξ_μέγεθος;
    ὀμφ_λεκτήρ_τρέχων = ομφ_λεκτήρ_κτίσον(λξ);
    if (!ὀμφ_λεκτήρ_τρέχων) {
        ομφ_τέλει(&ὀμφ_ὑπόδειγμα);
        return -1;
    }

    if (ομφ_λεκτήρ_ἀνάγνωθι(ὀμφ_λεκτήρ_τρέχων, ὁδ_tok) < 0) {
        fprintf(stderr, "ὀμφαλός: οὐκ ἠδυνήθην ἀναγνῶναι '%s'\n", ὁδ_tok);
        ομφ_λεκτήρ_κατάλυσον(ὀμφ_λεκτήρ_τρέχων);
        ὀμφ_λεκτήρ_τρέχων = NULL;
        ομφ_τέλει(&ὀμφ_ὑπόδειγμα);
        return -1;
    }

    snprintf(
        ὀμφ_ὄνομα_ὑποδ, sizeof(ὀμφ_ὄνομα_ὑποδ),
        "%s", ὄνομα
    );
    ὀμφ_ἀρχικοποιημένον = 1;
    ομφ_gpu_ἄρχε(&ὀμφ_ὑπόδειγμα);
    return 0;
}

