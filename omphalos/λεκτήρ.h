/*
 * ὀμφαλός_λεκτήρ.h — λεκτὴρ BPE τοῦ ὀμφαλοῦ
 *
 * Ψηφιοποιητὴς Byte-Pair Encoding (BPE) πρὸς ἄσκησιν καὶ συλλογισμόν.
 * Λεξικὸν ἐκ σώματος ἀσκεῖται· εἶτα σῴζεται/ἀναγιγνώσκεται ἐκ πτυχῆς.
 */

#ifndef ΟΜΦΑΛΟΣ_ΛΕΚΤΗΡ_H
#define ΟΜΦΑΛΟΣ_ΛΕΚΤΗΡ_H

#define ΟΜΦ_ΛΕΞ_ΣΗΜΕΙΟΝ_ΜΑΓΙΚΟΝ 0x4E4D4C31u  /* 'NML1' */
#define ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ      64           /* μέγιστον μῆκος λεξήματος (νῆμα) */
#define ΟΜΦ_ΛΕΞ_ΛΕΞΙΚΟΝ_ΠΡΟΕΠΙΛ  1024         /* μέγεθος λεξικοῦ προεπιλεγμένον */

/* ================================================================
 * ομφ_λέξημα_t — ἓν λέξημα ἐν λεξικῷ
 * ================================================================ */

typedef struct {
    char  νῆμα[ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ];
    int   μῆκος;       /* μῆκος νήματος ἐν ὀκτέτοις */
    float βαθμός;      /* βαθμὸς συγχωνεύσεως (ἐξ ἀσκήσεως) */
} ομφ_λέξημα_t;

/* ================================================================
 * ομφ_λεκτήρ_t — λεκτὴρ BPE
 * ================================================================ */

typedef struct {
    ομφ_λέξημα_t *λέξεις;       /* πίναξ λεξημάτων */
    int           λεξ_μέγεθος;
    int           λεξ_μέγ;
    int           μέγ_λεξ_μῆκ;  /* μέγιστον μῆκος λεξήματος (ὀκτέτοι) */
    /* λεξικὸν τεταγμένον πρὸς δυαδικὴν ζήτησιν */
    int          *τάξις;         /* δεῖκται εἰς λέξεις[], τεταγμένοι κατὰ νῆμα */
} ομφ_λεκτήρ_t;

/* ================================================================
 * συναρτήσεις
 * ================================================================ */

/*
 * ομφ_λεκτήρ_κτίσον — ἐκχωρεῖ λεκτῆρα νέον κενόν.
 * λεξ_μέγ: μέγεθος λεξικοῦ (προεπιλεγμένον: ΟΜΦ_ΛΕΞ_ΛΕΞΙΚΟΝ_ΠΡΟΕΠΙΛ).
 */
ομφ_λεκτήρ_t *ομφ_λεκτήρ_κτίσον(int λεξ_μέγ);

/*
 * ομφ_λεκτήρ_κατάλυσον — ἐλευθεροῖ λεκτῆρα.
 */
void ομφ_λεκτήρ_κατάλυσον(ομφ_λεκτήρ_t *λεκ);

/*
 * ομφ_λεκτήρ_ἄσκησον — οἰκοδομεῖ λεξικὸν BPE ἐκ σώματος.
 * Ἄρχεται ἐκ 256 σημείων ὀκτετικῶν· συγχωνεύει ζεύγη συχνότατα
 * ἕως οὗ λεξ_μέγ ἐπιτευχθῇ.
 * Ἀποδίδει 0 εἰ ἐπιτυχία, -1 εἰ σφάλμα.
 */
int ομφ_λεκτήρ_ἄσκησον(ομφ_λεκτήρ_t *λεκ, const char *σῶμα);

/*
 * ομφ_λεκτήρ_σῶσον — σῴζει λεκτῆρα εἰς δυαδικὴν πτυχήν.
 */
int ομφ_λεκτήρ_σῶσον(const ομφ_λεκτήρ_t *λεκ, const char *ὁδός);

/*
 * ομφ_λεκτήρ_ἀνάγνωθι — ἀναγιγνώσκει λεκτῆρα ἐκ δυαδικῆς πτυχῆς.
 * Ἀποδίδει 0 εἰ ἐπιτυχία, -1 εἰ σφάλμα.
 */
int ομφ_λεκτήρ_ἀνάγνωθι(ομφ_λεκτήρ_t *λεκ, const char *ὁδός);

/*
 * ομφ_λεκτήρ_τέμε — κωδικοποιεῖ κείμενον εἰς ἀκολουθίαν σημείων.
 * σημεῖα: ταμιεῖον πρὸς σημεῖα (ὁ καλῶν παρέχει· μέγεθος >= μῆκ_κειμ + 2).
 * ν_σημ: *ν_σημ τίθεται εἰς ἀριθμὸν σημείων.
 * ἀρχ/τελ: 1 = πρόσθες σημεῖον ἀρχῆς/τέλους.
 */
void ομφ_λεκτήρ_τέμε(
    const ομφ_λεκτήρ_t *λεκ, const char *κείμενον,
    int ἀρχ, int τελ, int *σημεῖα, int *ν_σημ
);

/*
 * ομφ_λεκτήρ_ἀπόδος — ἀποκωδικοποιεῖ σημεῖον εἰς νῆμα.
 * ἀποδίδει δείκτην εἰς ἐσωτερικὰς δομάς (μὴ ἐλευθέρωσῃς).
 */
const char *ομφ_λεκτήρ_ἀπόδος(const ομφ_λεκτήρ_t *λεκ, int σημεῖον);

#endif /* ΟΜΦΑΛΟΣ_ΛΕΚΤΗΡ_H */
