/*
 * ὀμφαλός_ἄσκησον.c — ἄσκησις μεταμορφωτοῦ ὀμφαλοῦ ἐκ γραμμῆς ἐντολῶν
 *
 * Χρῆσις: ὀμφαλός_ἄσκησον <ὄνομα> <ὁδὸς_σώματος> [ν_βήματα] [διαστ] [στρώμ] [κεφ]
 *
 *   <ὄνομα>         ὄνομα ὑποδείγματος· σῴζεται ἐν <ὄνομα>.bin καὶ <ὄνομα>.lex
 *   <ὁδὸς_σώματος>  πτυχὴ σώματος (UTF-8)
 *   [ν_βήματα]      βήματα ἀσκήσεως (προεπ. 1000)
 *   [διαστ]         διάστασις ὑποδείγματος (προεπ. 128)
 *   [στρώμ]         στρώματα μεταμορφωτοῦ (προεπ. 4)
 *   [κεφ]           κεφαλαὶ προσοχῆς (προεπ. 4)
 *
 * Εἰ <ὄνομα>.bin καὶ <ὄνομα>.lex ἤδη ὑπάρχουσιν, ἡ ἄσκησις συνεχίζεται
 * (παλαιὰ σταθμὰ φορτώνονται· νέα βήματα σωρεύονται).
 * Ἀσφάλεια (checkpoint) γίγνεται ἀνὰ ΑΣΦΑΛΕΙΑ_ΒΗΜΑΤΑ βήματα.
 */

#include "ὀμφαλός.h"
#include "λεκτήρ.h"
#include "phantasma/computo.h"

#include <math.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

/* ================================================================
 * προεπιλεγμέναι παράμετροι
 * ================================================================ */

#define ΠΡΟΕΠ_ΛΕΞ_ΜΕΓ       1024
#define ΠΡΟΕΠ_ΔΙΑΣΤΑΣΙΣ      128
#define ΠΡΟΕΠ_ΣΤΡΩΜΑΤΑ       4
#define ΠΡΟΕΠ_ΚΕΦΑΛΑΙ        4
#define ΠΡΟΕΠ_ΚΕΦΑΛΑΙ_ΚΤ     2
#define ΠΡΟΕΠ_ΜΗΚΟΣ_ΜΕΓ      128
#define ΠΡΟΕΠ_Ν_ΒΗΜΑΤΑ       1000
#define ΠΡΟΕΠ_ΑΚ_ΜΗΚΟΣ       64
#define ΠΡΟΕΠ_Ν_ΕΛΑΧ          4        /* παράθυρα ἀνὰ βῆμα (ἐλάχιστη δέσμη) */
#define ΠΡΟΕΠ_ΒΑΘΜΟΣ_ΜΕΓ     1e-4f   /* μέγιστος βαθμὸς μαθήσεως (ἀρχικῶς) */
#define ΠΡΟΕΠ_ΒΑΘΜΟΣ_ΕΛΑΧ    5e-6f   /* ἐλάχιστος βαθμὸς μαθήσεως (τελικῶς) */
#define ΠΡΟΕΠ_ΒΗΤΑ1           0.9f
#define ΠΡΟΕΠ_ΒΗΤΑ2           0.999f
#define ΠΡΟΕΠ_ΕΨΙΛΟΝ          1e-8f
#define ΠΡΟΕΠ_ΞΗΡΑΝΣΙΣ        1e-3f
#define ΑΓΓΕΛΙΑ_ΒΗΜΑΤΑ        100
#define ΑΣΦΑΛΕΙΑ_ΒΗΜΑΤΑ       500      /* σῶσον ἀνὰ Ν βήματα */

/* ================================================================
 * ἀνάγνωσις πτυχῆς
 * ================================================================ */

static char *ἀνάγνωθι_πτυχήν(const char *ὁδός)
{
    FILE *π = fopen(ὁδός, "rb");
    if (!π)
        return NULL;
    fseek(π, 0, SEEK_END);
    long μεγ = ftell(π);
    rewind(π);
    char *ταμ = malloc((size_t)μεγ + 1);
    if (!ταμ) {
        fclose(π);
        return NULL;
    }
    if (fread(ταμ, 1, (size_t)μεγ, π) != (size_t)μεγ) {
        free(ταμ);
        fclose(π);
        return NULL;
    }
    ταμ[μεγ] = '\0';
    fclose(π);
    return ταμ;
}

/* ================================================================
 * ἄσκησις — π_σπέρμα: κατάστασις RNG μεταξὺ ἀσφαλειῶν
 * ================================================================ */

static void ἄσκησον(
    ομφ_t *ομφ, ομφ_ἄσκησις_t *ἀσκ,
    const int *σημ_σώμ, int ν_σώμ,
    int ν_βήματα, int ἀκ_μῆκ,
    int βῆμα_ἀρχή, unsigned int *π_σπέρμα
) {
    int μμ = ομφ->σύνθεσις.μῆκος_μέγ;
    if (ἀκ_μῆκ > μμ - 1)
        ἀκ_μῆκ = μμ - 1;
    int μέγ_ἀρχ = ν_σώμ - ἀκ_μῆκ - 2;
    if (μέγ_ἀρχ <= 0)
        μέγ_ἀρχ = 1;

    for (int βῆμα = 0; βῆμα < ν_βήματα; βῆμα++) {
        ομφ_κλίσεις_μηδένισον(ἀσκ, &ομφ->σύνθεσις);
        float ζημ_ὅλ = 0.0f;

        for (int ἐλ = 0; ἐλ < ΠΡΟΕΠ_Ν_ΕΛΑΧ; ἐλ++) {
            *π_σπέρμα = *π_σπέρμα * 1664525u + 1013904223u;
            int ἀρχ   = (int)(*π_σπέρμα % (unsigned int)μέγ_ἀρχ);

            ομφ_κατάστ_ἀποκατάστησον(ομφ);
            for (int τ = 0; τ < ἀκ_μῆκ && ἀρχ + τ + 1 < ν_σώμ; τ++) {
                int σημ  = σημ_σώμ[ἀρχ + τ];
                int στόχ = σημ_σώμ[ἀρχ + τ + 1];
                ομφ_δρόμος_μνήμη(ομφ, ἀσκ, σημ, τ);
                ζημ_ὅλ += ομφ_ἀναδρομή(ομφ, ἀσκ, σημ, στόχ, τ);
            }
        }

        ομφ_κλίσεις_κεῖρον(ἀσκ, &ομφ->σύνθεσις, 1.0f);
        /* cosine annealing: βαθμὸς φθίνει ἀπὸ μεγ πρὸς ἐλάχ */
        int β_ἀπ = βῆμα_ἀρχή + βῆμα;
        int β_ὅλ = βῆμα_ἀρχή + ν_βήματα;
        float βμ = ΠΡΟΕΠ_ΒΑΘΜΟΣ_ΕΛΑΧ + 0.5f * (ΠΡΟΕΠ_ΒΑΘΜΟΣ_ΜΕΓ - ΠΡΟΕΠ_ΒΑΘΜΟΣ_ΕΛΑΧ)
        * (1.0f + cosf(3.14159265f * (float)β_ἀπ / (float)β_ὅλ));
        ομφ_βῆμα_ἀδάμ(
            ομφ, ἀσκ, βμ, ΠΡΟΕΠ_ΒΗΤΑ1, ΠΡΟΕΠ_ΒΗΤΑ2,
            ΠΡΟΕΠ_ΕΨΙΛΟΝ, ΠΡΟΕΠ_ΞΗΡΑΝΣΙΣ
        );

        if ((βῆμα_ἀρχή + βῆμα + 1) % ΑΓΓΕΛΙΑ_ΒΗΜΑΤΑ == 0) {
            float ζμ = ζημ_ὅλ / (float)(ἀκ_μῆκ * ΠΡΟΕΠ_Ν_ΕΛΑΧ);
            printf(
                "  βῆμα %5d  ζημία=%.4f  ἀμηχανία=%.2f\n",
                βῆμα_ἀρχή + βῆμα + 1, ζμ, expf(ζμ)
            );
            fflush(stdout);
        }
    }
}

/* ================================================================
 * main
 * ================================================================ */

int main(int argc, char **argv)
{
    if (argc < 3) {
        fprintf(
            stderr,
            "χρῆσις: %s <ὄνομα> <ὁδὸς_σώματος> [ν_βήματα] [διαστ] [στρώμ] [κεφ]\n"
            "  προεπιλ: ν_βήματα=%d διαστ=%d στρώμ=%d κεφ=%d κτ=%d\n"
            "  εἰ <ὄνομα>.bin ὑπάρχει, ἡ ἄσκησις συνεχίζεται\n",
            argv[0], ΠΡΟΕΠ_Ν_ΒΗΜΑΤΑ, ΠΡΟΕΠ_ΔΙΑΣΤΑΣΙΣ, ΠΡΟΕΠ_ΣΤΡΩΜΑΤΑ,
            ΠΡΟΕΠ_ΚΕΦΑΛΑΙ, ΠΡΟΕΠ_ΚΕΦΑΛΑΙ_ΚΤ
        );
        return 1;
    }
    const char *ὄνομα      = argv[1];
    const char *ὁδὸς_σώμ   = argv[2];
    int ν_βήματα  = argc >= 4 ? atoi(argv[3]) : ΠΡΟΕΠ_Ν_ΒΗΜΑΤΑ;
    int διαστ     = argc >= 5 ? atoi(argv[4]) : ΠΡΟΕΠ_ΔΙΑΣΤΑΣΙΣ;
    int στρώμ     = argc >= 6 ? atoi(argv[5]) : ΠΡΟΕΠ_ΣΤΡΩΜΑΤΑ;
    int κεφ       = argc >= 7 ? atoi(argv[6]) : ΠΡΟΕΠ_ΚΕΦΑΛΑΙ;
    int κεφ_κτ    = (κεφ >= 4) ? κεφ / 2 : ΠΡΟΕΠ_ΚΕΦΑΛΑΙ_ΚΤ;

    printf("=== ὀμφαλός_ἄσκησον: %s ===\n\n", ὄνομα);

    int gpu = pfr_computo_initia();
    printf("ὑπολογιστής: %s\n\n", gpu == 0 ? "GPU" : "CPU");

    char ὁδ_bin[512], ὁδ_lex[512];
    snprintf(ὁδ_bin, sizeof(ὁδ_bin), "%s.bin", ὄνομα);
    snprintf(ὁδ_lex, sizeof(ὁδ_lex), "%s.lex", ὄνομα);

    /* --- 1. ἀνάγνωθι σῶμα --- */
    printf("1. ἀναγιγνώσκω σῶμα ἐκ '%s'...\n", ὁδὸς_σώμ);
    char *σῶμα = ἀνάγνωθι_πτυχήν(ὁδὸς_σώμ);
    if (!σῶμα || strlen(σῶμα) < 10) {
        fprintf(stderr, "σφάλμα: σῶμα κενὸν ἢ οὐχ εὕρηται\n");
        pfr_computo_fini();
        return 1;
    }
    printf("   μῆκος: %zu χαρακτῆρες\n\n", strlen(σῶμα));

    /* --- 2. λεκτήρ: πειράθητι φορτῶσαι ἢ ἄσκησον νέον --- */
    ομφ_λεκτήρ_t *λεκ = ομφ_λεκτήρ_κτίσον(ΠΡΟΕΠ_ΛΕΞ_ΜΕΓ);
    if (!λεκ) {
        fprintf(stderr, "σφάλμα: ομφ_λεκτήρ_κτίσον ἀπέτυχε\n");
        free(σῶμα);
        pfr_computo_fini();
        return 1;
    }

    int ἐπανάληψις = 0;
    {
        FILE *πτ = fopen(ὁδ_lex, "rb");
        if (πτ) { fclose(πτ); }
        if (πτ && ομφ_λεκτήρ_ἀνάγνωθι(λεκ, ὁδ_lex) == 0) {
            printf(
                "2. λεκτὴρ ἐφορτώθη ἐκ '%s' (λεξ=%d)\n\n",
                ὁδ_lex, λεκ->λεξ_μέγεθος
            );
            ἐπανάληψις = 1;
        } else {
            printf("2. ἀσκῶ λεκτῆρα BPE (λεξ_μέγ=%d)...\n", ΠΡΟΕΠ_ΛΕΞ_ΜΕΓ);
            if (ομφ_λεκτήρ_ἄσκησον(λεκ, σῶμα) < 0) {
                fprintf(stderr, "σφάλμα: ἄσκησις λεκτῆρος ἀπέτυχε\n");
                free(σῶμα);
                ομφ_λεκτήρ_κατάλυσον(λεκ);
                pfr_computo_fini();
                return 1;
            }
            printf("   λεξ_μέγεθος: %d\n", λεκ->λεξ_μέγεθος);
            if (ομφ_λεκτήρ_σῶσον(λεκ, ὁδ_lex) < 0)
                fprintf(stderr, "προειδοποίησις: λεκτὴρ σωθῆναι οὐκ ἠδυνήθη\n");
            else
                printf("   λεκτὴρ ἐσώθη: %s\n\n", ὁδ_lex);
        }
    }

    int μῆκ_σώμ = (int)strlen(σῶμα);
    int *σημεῖα  = malloc((size_t)(μῆκ_σώμ + 4) * sizeof(int));
    if (!σημεῖα) {
        free(σῶμα);
        ομφ_λεκτήρ_κατάλυσον(λεκ);
        pfr_computo_fini();
        return 1;
    }
    int ν_σημ = 0;
    ομφ_λεκτήρ_τέμε(λεκ, σῶμα, 0, 0, σημεῖα, &ν_σημ);
    printf("   σημεῖα ἐν σώματι: %d\n\n", ν_σημ);
    free(σῶμα);

    /* --- 3. ὑπόδειγμα: φόρτωσον ἢ ἄρχε νέον --- */
    ομφ_t ομφ;
    if (ἐπανάληψις && ομφ_ἀνάγνωθι(&ομφ, ὁδ_bin) == 0) {
        printf(
            "3. ὑπόδειγμα ἐφορτώθη ἐκ '%s' (διαστ=%d στρώμ=%d λεξ=%d)\n\n",
            ὁδ_bin, ομφ.σύνθεσις.διάστασις, ομφ.σύνθεσις.στρώματα,
            ομφ.σύνθεσις.λεξ_μέγεθος
        );
        διαστ  = ομφ.σύνθεσις.διάστασις;
        στρώμ  = ομφ.σύνθεσις.στρώματα;
        κεφ    = ομφ.σύνθεσις.κεφαλαί;
        κεφ_κτ = ομφ.σύνθεσις.κεφαλαί_κτ;
    } else {
        ἐπανάληψις = 0;
        int κρυ = (διαστ * 8 / 3 + 7) & ~7;
        ομφ_σύνθεσις_t σύνθ = {
            .διάστασις    = διαστ,
            .διάστ_κρυ    = κρυ,
            .στρώματα     = στρώμ,
            .κεφαλαί      = κεφ,
            .κεφαλαί_κτ   = κεφ_κτ,
            .λεξ_μέγεθος  = λεκ->λεξ_μέγεθος,
            .μῆκος_μέγ    = ΠΡΟΕΠ_ΜΗΚΟΣ_ΜΕΓ,
        };
        printf(
            "3. ἄρχω ὑπόδειγμα νέον (διαστ=%d κρυ=%d στρώμ=%d κεφ=%d"
            " κτ=%d λεξ=%d μμ=%d)...\n",
            διαστ, κρυ, στρώμ, κεφ, κεφ_κτ,
            λεκ->λεξ_μέγεθος, ΠΡΟΕΠ_ΜΗΚΟΣ_ΜΕΓ
        );
        if (ομφ_ἄρχε_τυχαίως(&ομφ, &σύνθ, 42u) < 0) {
            fprintf(stderr, "σφάλμα: ομφ_ἄρχε_τυχαίως ἀπέτυχε\n");
            free(σημεῖα);
            ομφ_λεκτήρ_κατάλυσον(λεκ);
            pfr_computo_fini();
            return 1;
        }
        size_t ν_παρ = ομφ_μέγεθος_σταθμῶν(&σύνθ, 1);
        printf(
            "   παράμετροι: %zu δεκαδικά = %.1f KB\n\n",
            ν_παρ, ν_παρ * sizeof(float) / 1024.0
        );
    }

    /* --- 4. ἄσκησον ἐν τμήμασι μετ᾽ ἀσφαλείας --- */
    printf(
        "4. ἀσκῶ (%d βήματα, ἀκ_μῆκ=%d, ἐλαχ=%d, ἀσφάλεια=%d)...\n",
        ν_βήματα, ΠΡΟΕΠ_ΑΚ_ΜΗΚΟΣ, ΠΡΟΕΠ_Ν_ΕΛΑΧ, ΑΣΦΑΛΕΙΑ_ΒΗΜΑΤΑ
    );
    ομφ_ἄσκησις_t ἀσκ;
    if (ομφ_ἄσκησις_ἄρχε(&ἀσκ, &ομφ.σύνθεσις) < 0) {
        fprintf(stderr, "σφάλμα: ομφ_ἄσκησις_ἄρχε ἀπέτυχε\n");
        ομφ_τέλει(&ομφ);
        free(σημεῖα);
        ομφ_λεκτήρ_κατάλυσον(λεκ);
        pfr_computo_fini();
        return 1;
    }

    unsigned int σπέρμα = 42u;
    int βήματα_γενόμενα = 0;
    while (βήματα_γενόμενα < ν_βήματα) {
        int δέσμη = ΑΣΦΑΛΕΙΑ_ΒΗΜΑΤΑ;
        if (βήματα_γενόμενα + δέσμη > ν_βήματα)
            δέσμη = ν_βήματα - βήματα_γενόμενα;

        ἄσκησον(
            &ομφ, &ἀσκ, σημεῖα, ν_σημ, δέσμη, ΠΡΟΕΠ_ΑΚ_ΜΗΚΟΣ,
            βήματα_γενόμενα, &σπέρμα
        );
        βήματα_γενόμενα += δέσμη;

        /* ἐνδιάμεσος ἀσφάλεια */
        if (ομφ_σῶσον(&ομφ, ὁδ_bin) == 0)
            printf(
                "  [ἀσφάλεια: βῆμα %d/%d -> %s]\n",
                βήματα_γενόμενα, ν_βήματα, ὁδ_bin
            );
        else
            fprintf(stderr, "  προειδοποίησις: ἀσφάλεια ἀπέτυχε\n");
        fflush(stdout);
    }

    ομφ_ἄσκησις_τέλει(&ἀσκ);
    free(σημεῖα);
    printf("\n");

    /* --- 5. τελικὴ σωτηρία (ἤδη γεγονυῖα δι᾽ ἀσφαλείας) --- */
    printf("5. ὑπόδειγμα ἐσώθη: %s\n\n", ὁδ_bin);

    ομφ_τέλει(&ομφ);
    ομφ_λεκτήρ_κατάλυσον(λεκ);
    pfr_computo_fini();

    printf("=== ἄσκησις τετελεσμένη ===\n");
    return 0;
}
