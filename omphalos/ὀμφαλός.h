/*
 * ὀμφαλός.h — μεταμορφωτὴς ὀμφαλός: κοινοὶ τύποι
 *
 * Ἀρχιτεκτονία Llama-2: RMSNorm, RoPE, SwiGLU, GQA.
 * Πράξεις πινάκων διὰ phantasma/computo.h (pfr_matvec_f κτλ.).
 */

#ifndef ΟΜΦΑΛΟΣ_H
#define ΟΜΦΑΛΟΣ_H

#include <stddef.h>

/* ================================================================
 * σύνθεσις
 * ================================================================ */

typedef struct {
    int διάστασις;        /* d_model */
    int διάστ_κρυ;        /* d_ff (FFN κρυπτή) */
    int στρώματα;         /* ἀριθμὸς στρωμάτων */
    int κεφαλαί;          /* κεφαλαὶ ἐρωτήσεως */
    int κεφαλαί_κτ;       /* κεφαλαὶ κλειδός/τιμῆς */
    int λεξ_μέγεθος;      /* μέγεθος λεξικοῦ */
    int μῆκος_μέγ;        /* μέγιστον μῆκος ἀκολουθίας */
} ομφ_σύνθεσις_t;

/* ================================================================
 * σταθμά — δεῖκται εἰς mmap'd ἢ malloc'd ἐπίπεδον ταμιεῖον
 *
 * Διάταξις ἐν ταμιείῳ (συνεχής, κατὰ γραμμάς — ordo llama2.c):
 *   ἴχνη      (λεξ_μέγεθος, διάστασις)
 *   rms_πρ    (στρώματα, διάστασις)
 *   wq        (στρώματα, διάστασις, διάστασις)
 *   wk        (στρώματα, κτ_διαστ, διάστασις)    κτ_διαστ = (διάστασις/κεφαλαί)*κεφαλαί_κτ
 *   wv        (στρώματα, κτ_διαστ, διάστασις)
 *   wo        (στρώματα, διάστασις, διάστασις)
 *   rms_δδ    (στρώματα, διάστασις)
 *   w1        (στρώματα, διάστ_κρυ, διάστασις)
 *   w2        (στρώματα, διάστασις, διάστ_κρυ)
 *   w3        (στρώματα, διάστ_κρυ, διάστασις)
 *   rms_τέλ   (διάστασις)
 * ================================================================ */

typedef struct {
    float *ἴχνη;
    float *rms_πρ;
    float *rms_δδ;
    float *wq;
    float *wk;
    float *wv;
    float *wo;
    float *w1;
    float *w2;
    float *w3;
    float *rms_τέλ;
    float *σλεξ;   /* == ἴχνη (κοινὰ σταθμά) ἢ χωριστὴ ἐκχώρησις */
} ομφ_σταθμά_t;

/* ================================================================
 * κατάστασις δρόμου — ἐνεργοποιήσεις καὶ κρύπτη ΚΤ
 * ================================================================ */

typedef struct {
    float *x;            /* (διάστασις,) */
    float *xb;           /* (διάστασις,) */
    float *xb2;          /* (διάστασις,) */
    float *hb;           /* (διάστ_κρυ,) */
    float *hb2;          /* (διάστ_κρυ,) */
    float *q;            /* (διάστασις,) */
    float *k;            /* (κτ_διαστ,) */
    float *v;            /* (κτ_διαστ,) */
    float *πρ;           /* (κεφαλαί, μῆκος_μέγ) */
    float *λογιταί;      /* (λεξ_μέγεθος,) */
    float *κρύπτη_κλείς; /* (στρώματα, μῆκος_μέγ, κτ_διαστ) */
    float *κρύπτη_τιμή;  /* (στρώματα, μῆκος_μέγ, κτ_διαστ) */
} ομφ_κατάστ_t;

/* ================================================================
 * ομφ_t — πλῆρες ὑπόδειγμα
 * ================================================================ */

typedef struct {
    ομφ_σύνθεσις_t  σύνθεσις;
    ομφ_σταθμά_t    σταθμά;
    ομφ_κατάστ_t    κατάστ;
    int             σταθμά_κοινά;  /* 1: σλεξ == ἴχνη */
    float          *δεδομένα;      /* ἐπίπεδον ταμιεῖον (malloc'd) */
    size_t          μέγεθος_δεδ;
} ομφ_t;

/* ================================================================
 * ομφ_ἄσκησις_t — κατάστασις ἀσκήσεως (κλίσεις + AdamW)
 * ================================================================ */

typedef struct {
    /* κλίσεις (σωρεύονται πρὸ βήματος AdamW) */
    float       *κλίσ_δεδ;
    ομφ_σταθμά_t κλίσεις;
    /* ὁρμὴ πρώτη AdamW (m) */
    float       *ὁρμή_δεδ;
    ομφ_σταθμά_t ὁρμή;
    /* δύναμις δευτέρα AdamW (v) */
    float       *δύναμις_δεδ;
    ομφ_σταθμά_t δύναμις;

    /*
        ἐνεργοποιήσεις μνημονευθεῖσαι πρὸς ἀναδρομήν (διαστάσεις μιᾶς θέσεως) */
    float *μνήμη_x;       /* (στρώματα+1, διάστασις) — x πρὸ ἑκάστου στρώματος */
    float *μνήμη_xb_πρ;   /* (στρώματα, διάστασις)   — μετὰ rmsnorm προσοχῆς */
    float *μνήμη_xb_δδ;   /* (στρώματα, διάστασις)   — μετὰ rmsnorm δικτύου */
    float *μνήμη_q;        /* (στρώματα, διάστασις)   — q μετὰ RoPE */
    float *μνήμη_k;        /* (στρώματα, κτ_διαστ)    — k εἰς τρέχουσαν θέσιν */
    float *μνήμη_πρ;       /*
        (στρώματα, κεφαλαί, μῆκος_μέγ) — βαθμοὶ προσοχῆς */
    float *μνήμη_hb;       /* (στρώματα, διάστ_κρυ) — w1 @ xb_δδ (πρὸ SwiGLU) */
    float *μνήμη_hb2;      /* (στρώματα, διάστ_κρυ) — w3 @ xb_δδ */
    float *μνήμη_χτέλ;     /* (διάστασις) — x μετὰ rmsnorm τέλους */

    int  βῆμα;             /* ἀριθμὸς βημάτων AdamW (πρὸς διόρθωσιν μεροληψίας) */
    long σημεῖα_κατεργ;    /* σημεῖα κατεργασθέντα ἐν ὅλῳ */
} ομφ_ἄσκησις_t;

/* ================================================================
 * συναρτήσεις (ὀμφαλός_δρόμος.c)
 * ================================================================ */

/*
 * ομφ_μέγεθος_σταθμῶν — ἀριθμὸς δεκαδικῶν δι᾽ ἓν σύνολον σταθμῶν.
 * Εἰ σταθμά_κοινά, σλεξ οὐ λογίζεται.
 */
size_t ομφ_μέγεθος_σταθμῶν(const ομφ_σύνθεσις_t *σ, int κοινά);

/*
 * ομφ_σταθμά_ἄρχε_δείκτ — τίθησι πάντας δείκτας ἐν π ὡς δεικνύωσιν εἰς δεδομένα.
 * Ἀποδίδει: δείκτην μετὰ τὰ ἔσχατα σταθμά.
 */
float *ομφ_σταθμά_ἄρχε_δείκτ(
    ομφ_σταθμά_t *π, const ομφ_σύνθεσις_t *σ, float *δεδομένα
);

/*
 * ομφ_ἄρχε_τυχαίως — κτίζει ομφ_t νέον μετὰ τυχαίων σταθμῶν.
 * σπέρμα: σπέρμα RNG πρὸς ἀρχικοποίησιν. Ἀποδίδει 0 εἰ ἐπιτυχία.
 */
int ομφ_ἄρχε_τυχαίως(
    ομφ_t *ομφ, const ομφ_σύνθεσις_t *σύνθεσις, unsigned int σπέρμα
);

/*
 * ομφ_ἀνάγνωθι — ἀναγιγνώσκει ομφ_t ἐκ δυαδικῆς πτυχῆς. Ἀποδίδει 0 εἰ ἐπιτυχία.
 */
int ομφ_ἀνάγνωθι(ομφ_t *ομφ, const char *ὁδός);

/*
 * ομφ_σῶσον — σῴζει ομφ_t εἰς δυαδικὴν πτυχήν. Ἀποδίδει 0 εἰ ἐπιτυχία.
 */
int ομφ_σῶσον(const ομφ_t *ομφ, const char *ὁδός);

/*
 * ομφ_τέλει — ἐλευθεροῖ πάσας μνήμας ἐν ομφ_t.
 */
void ομφ_τέλει(ομφ_t *ομφ);

/*
 * ομφ_κατάστ_ἀποκατάστησον — τίθησι πάσας ἐνεργοποιήσεις καὶ κρύπτην ΚΤ εἰς 0.
 */
void ομφ_κατάστ_ἀποκατάστησον(ομφ_t *ομφ);

/*
 * ομφ_gpu_ἄρχε — ἐκχωρεῖ GPU ταμιεῖα πρὸς σταθμὰ καὶ ἐνεργοποιήσεις.
 * Σταθμὰ ἅπαξ πέμπονται· ἐνεργοποιήσεις συγχρονίζονται κατὰ πρᾶξιν.
 * Εἰ GPU ἀπουσιάζει, ἀποδίδει -1 καὶ πᾶσαι πράξεις ἐν CPU μένουσιν.
 * Ἡ ἐφαρμογὴ οὐ δεῖ φροντίζειν — ἡ αὐτὴ πρόσοψις μετὰ ἢ ἄνευ GPU.
 */
int ομφ_gpu_ἄρχε(ομφ_t *ομφ);

/*
 * ομφ_gpu_τέλει — ἐλευθεροῖ GPU ταμιεῖα.
 */
void ομφ_gpu_τέλει(void);

/*
 * ομφ_δρόμος — βῆμα πρόσω πρὸς συλλογισμόν (ἐνημερώνει ομφ->κατάστ.λογιταί).
 * ἀποδίδει ομφ->κατάστ.λογιταί (οὐκ ἐκχωρεῖται χωριστῶς).
 */
float *ομφ_δρόμος(ομφ_t *ομφ, int σημεῖον, int θέσις);

/*
 * ομφ_δρόμος_μνήμη — βῆμα πρόσω πρὸς ἄσκησιν· σῴζει ἐνεργοποιήσεις ἐν ἀσκ.
 * ἀποδίδει ομφ->κατάστ.λογιταί.
 */
float *ομφ_δρόμος_μνήμη(ομφ_t *ομφ, ομφ_ἄσκησις_t *ἀσκ, int σημεῖον, int θέσις);

/* ================================================================
 * συναρτήσεις (ὀμφαλός_ἄσκησις.c)
 * ================================================================ */

/*
 * ομφ_ἄσκησις_ἄρχε — ἐκχωρεῖ κλίσεις, ὁρμάς, μνήμας.
 * Ἀποδίδει 0 εἰ ἐπιτυχία.
 */
int  ομφ_ἄσκησις_ἄρχε(ομφ_ἄσκησις_t *ἀσκ, const ομφ_σύνθεσις_t *σ);

/*
 * ομφ_ἄσκησις_τέλει — ἐλευθεροῖ πάσας μνήμας ἐν ἀσκ.
 */
void ομφ_ἄσκησις_τέλει(ομφ_ἄσκησις_t *ἀσκ);

/*
 * ομφ_κλίσεις_μηδένισον — τίθησι πάσας κλίσεις εἰς 0 πρὸ νέας δέσμης.
 */
void ομφ_κλίσεις_μηδένισον(ομφ_ἄσκησις_t *ἀσκ, const ομφ_σύνθεσις_t *σ);

/*
 * ομφ_ἀναδρομή — ὑπολογίζει κλίσεις δι᾽ ἓν θέσιν.
 * σημεῖον: σημεῖον εἰσόδου (πρὸς βῆμα ἰχνῶν).
 * σημεῖον_στόχος: σημεῖον ἀναμενόμενον (δείκτης ἐν λεξικῷ).
 * Σωρεύει ἐν ἀσκ->κλίσεις. Ἀποδίδει ζημίαν (cross-entropy loss).
 */
float ομφ_ἀναδρομή(
    ομφ_t *ομφ, ομφ_ἄσκησις_t *ἀσκ,
    int σημεῖον, int σημεῖον_στόχος, int θέσις
);

/*
 * ομφ_κλίσεις_κεῖρον — κείρει κανόνα ὁλικῶς πρὸς μέγ_κανόνα (gradient clipping).
 * ἀποδίδει κανόνα πρὸ κουρᾶς.
 */
float ομφ_κλίσεις_κεῖρον(
    ομφ_ἄσκησις_t *ἀσκ, const ομφ_σύνθεσις_t *σ,
    float μέγ_κανών
);

/*
 * ομφ_βῆμα_ἀδάμ — βῆμα AdamW: ἐνημερώνει σταθμά, ἀναθέτει κλίσεις.
 */
void ομφ_βῆμα_ἀδάμ(
    ομφ_t *ομφ, ομφ_ἄσκησις_t *ἀσκ,
    float βαθμός_μαθήσεως, float βῆτα1, float βῆτα2,
    float ἐψιλον, float ξήρανσις
);

#endif /* ΟΜΦΑΛΟΣ_H */
