/* ================================================================
 * ἀνάγνωσις σώματος
 * ================================================================ */

static char *ἀνάγνωθι_πτυχήν(const char *ὁδός)
{
    FILE *π = fopen(ὁδός, "rb");
    if (!π)
        return NULL;
    fseek(π, 0, SEEK_END);
    long μεγ = ftell(π);
    fseek(π, 0, SEEK_SET);
    char *ταμ = malloc((size_t)μεγ + 1);
    if (!ταμ) {
        fclose(π);
        return NULL;
    }
    fread(ταμ, 1, (size_t)μεγ, π);
    ταμ[μεγ] = '\0';
    fclose(π);
    return ταμ;
}

static char *ἀνάγνωθι_σῶμα(void)
{
    const char *πτυχαί[] = {
        "oracula/corpus/dieta_1526_i.txt",
        "oracula/corpus/dieta_1526_ii.txt",
        "oracula/corpus/dieta_1526_iii.txt",
        "oracula/corpus/dieta_1526_iv.txt",
        "oracula/corpus/dieta_1526_v.txt",
        "oracula/corpus/dieta_1526_vi.txt",
    };
    int ν_πτυχ = 6;

    /* σύνολον μεγέθους */
    size_t μεγ_ὅλ = 0;
    char **μέρη     = malloc((size_t)ν_πτυχ * sizeof(char *));
    if (!μέρη)
        return NULL;
    for (int i = 0; i < ν_πτυχ; i++) {
        μέρη[i] = ἀνάγνωθι_πτυχήν(πτυχαί[i]);
        if (μέρη[i])
            μεγ_ὅλ += strlen(μέρη[i]) + 1;
        else
            fprintf(
                stderr,
                "προειδοποίησις: οὐκ ἠδυνήθην ἀναγνῶναι '%s'\n",
                πτυχαί[i]
            );
    }

    char *σῶμα = malloc(μεγ_ὅλ + 1);
    if (!σῶμα) {
        for (int i = 0; i < ν_πτυχ; i++)
            free(μέρη[i]);
        free(μέρη);
        return NULL;
    }
    σῶμα[0] = '\0';
    for (int i = 0; i < ν_πτυχ; i++) {
        if (μέρη[i]) {
            strcat(σῶμα, μέρη[i]);
            strcat(σῶμα, "\n");
            free(μέρη[i]);
        }
    }
    free(μέρη);
    return σῶμα;
}

/* ================================================================
 * ἄσκησις
 * ================================================================ */

#define Ν_ΒΗΜΑΤΑ       20
#define ΜΗΚΟΣ_ΑΚ       48
#define Ν_ΕΛΑΧ          4       /* παράθυρα ἀνὰ βῆμα (ἐλάχιστη δέσμη) */
#define ΒΑΘΜΟΣ_ΜΑΘ    3e-3f
#define ΒΗΤΑ1          0.9f
#define ΒΗΤΑ2          0.999f
#define ΕΨΙΛΟΝ         1e-8f
#define ΞΗΡΑΝΣΙΣ       1e-2f
#define ΑΓΓΕΛΙΑ_ΒΗΜΑΤΑ  10

static void ἄσκησον(
    ομφ_t *ομφ, ομφ_ἄσκησις_t *ἀσκ,
    const int *σημ_σώμ, int ν_σώμ,
    int ν_βήματα
) {
    int μμ = ομφ->σύνθεσις.μῆκος_μέγ;
    int ἀκ_μῆκ = ΜΗΚΟΣ_ΑΚ < μμ ? ΜΗΚΟΣ_ΑΚ : μμ - 1;
    unsigned int σπέρμα = 42u;
    int μέγ_ἀρχ = ν_σώμ - ἀκ_μῆκ - 2;
    if (μέγ_ἀρχ <= 0)
        μέγ_ἀρχ = 1;

    for (int βῆμα = 0; βῆμα < ν_βήματα; βῆμα++) {
        ομφ_κλίσεις_μηδένισον(ἀσκ, &ομφ->σύνθεσις);
        float ζημ_ὅλ = 0.0f;

        for (int ἐλ = 0; ἐλ < Ν_ΕΛΑΧ; ἐλ++) {
            σπέρμα = σπέρμα * 1664525u + 1013904223u;
            int ἀρχ  = (int)(σπέρμα % (unsigned int)μέγ_ἀρχ);

            ομφ_κατάστ_ἀποκατάστησον(ομφ);
            for (int τ = 0; τ < ἀκ_μῆκ && ἀρχ + τ + 1 < ν_σώμ; τ++) {
                int σημ   = σημ_σώμ[ἀρχ + τ];
                int στόχ = σημ_σώμ[ἀρχ + τ + 1];
                ομφ_δρόμος_μνήμη(ομφ, ἀσκ, σημ, τ);
                ζημ_ὅλ += ομφ_ἀναδρομή(ομφ, ἀσκ, σημ, στόχ, τ);
            }
        }

        ομφ_κλίσεις_κεῖρον(ἀσκ, &ομφ->σύνθεσις, 1.0f);
        ομφ_βῆμα_ἀδάμ(ομφ, ἀσκ, ΒΑΘΜΟΣ_ΜΑΘ, ΒΗΤΑ1, ΒΗΤΑ2, ΕΨΙΛΟΝ, ΞΗΡΑΝΣΙΣ);

        if ((βῆμα + 1) % ΑΓΓΕΛΙΑ_ΒΗΜΑΤΑ == 0) {
            float ζημ_μέσ = ζημ_ὅλ / (float)(ἀκ_μῆκ * Ν_ΕΛΑΧ);
            printf(
                "  βῆμα %4d/%d  ζημία=%.4f  ἀμηχανία=%.2f\n",
                βῆμα + 1, ν_βήματα,
                ζημ_μέσ, expf(ζημ_μέσ)
            );
            fflush(stdout);
        }
    }
}

/* ================================================================
 * γέννησις κειμένου
 * ================================================================ */

static void γέννησον_καὶ_τύπωσον(
    ομφ_t *ομφ, ομφ_λεκτήρ_t *λεκ,
    const char *προτροπή, int ν_γεννήσεις
) {
    ομφ_σύνθεσις_t *σ = &ομφ->σύνθεσις;
    int Λξ = σ->λεξ_μέγεθος;

    int ν_ταμ      = (int)strlen(προτροπή) + 4;
    int *σημεῖα = malloc((size_t)ν_ταμ * sizeof(int));
    if (!σημεῖα)
        return;
    int ν_σημ = 0;
    ομφ_λεκτήρ_τέμε(λεκ, προτροπή, 1, 0, σημεῖα, &ν_σημ);

    ομφ_κατάστ_ἀποκατάστησον(ομφ);
    float *λογιταί = NULL;
    for (int τ = 0; τ < ν_σημ && τ < σ->μῆκος_μέγ - 1; τ++)
        λογιταί = ομφ_δρόμος(ομφ, σημεῖα[τ], τ);
    int θέσ = ν_σημ < σ->μῆκος_μέγ ? ν_σημ : σ->μῆκος_μέγ - 1;
    free(σημεῖα);

    printf("%s", προτροπή);
    fflush(stdout);

    unsigned int σπέρμα = 12345u;
    for (int i = 0; i < ν_γεννήσεις && θέσ < σ->μῆκος_μέγ && λογιταί; i++) {
        /* δειγματοληψία μετὰ θερμότητος 0.9 */
        float *πιθ = malloc((size_t)Λξ * sizeof(float));
        if (!πιθ)
            break;
        memcpy(πιθ, λογιταί, (size_t)Λξ * sizeof(float));
        for (int j = 0; j < Λξ; j++)
            πιθ[j] /= 0.9f;
        float μέγ = πιθ[0];
        for (int j = 1; j < Λξ; j++)
            if (πιθ[j] > μέγ)
                μέγ = πιθ[j];
        float ἄθρ = 0.0f;
        for (int j = 0; j < Λξ; j++) {
            πιθ[j] = expf(πιθ[j] - μέγ);
            ἄθρ += πιθ[j];
        }
        for (int j = 0; j < Λξ; j++)
            πιθ[j] /= ἄθρ;
        σπέρμα  = σπέρμα * 1664525u + 1013904223u;
        float τυχ  = ((float)(σπέρμα & 0x7fffffff)) / (float)0x7fffffff;
        float σαθ  = 0.0f;
        int ἑπόμ = Λξ - 1;
        for (int j = 0; j < Λξ; j++) {
            σαθ += πιθ[j];
            if (τυχ < σαθ) {
                ἑπόμ = j;
                break;
            }
        }
        free(πιθ);
        if (ἑπόμ == 2)
            break;
        printf("%s", ομφ_λεκτήρ_ἀπόδος(λεκ, ἑπόμ));
        fflush(stdout);
        λογιταί = ομφ_δρόμος(ομφ, ἑπόμ, θέσ++);
    }
    printf("\n");
}

/* ================================================================
 * main
 * ================================================================ */

int main(void)
{
    printf("=== δοκιμή_ὀμφαλός ===\n\n");

    /* ἄρχε ὑπολογιστήν (GPU ἢ CPU ὡς ἐφεδρεία) */
    int gpu = pfr_computo_initia();
    printf("ὑπολογιστής: %s\n\n", gpu == 0 ? "GPU" : "CPU");

    /* --- 1. ἀνάγνωθι σῶμα --- */
    printf("1. ἀναγιγνώσκω σῶμα...\n");
    char *σῶμα = ἀνάγνωθι_σῶμα();
    if (!σῶμα || strlen(σῶμα) < 10) {
        fprintf(stderr, "σῶμα κενὸν ἢ οὐχ εὕρηται\n");
        return 1;
    }
    printf("   μῆκος σώματος: %zu χαρακτῆρες\n\n", strlen(σῶμα));

    /* --- 2. ἄσκησον λεκτῆρα --- */
    printf("2. ἀσκῶ λεκτῆρα BPE (λεξ_μέγ=512)...\n");
    ομφ_λεκτήρ_t *λεκ = ομφ_λεκτήρ_κτίσον(1024);
    if (!λεκ || ομφ_λεκτήρ_ἄσκησον(λεκ, σῶμα) < 0) {
        fprintf(stderr, "ἄσκησις λεκτῆρος ἀπέτυχε\n");
        free(σῶμα);
        return 1;
    }
    printf("   λεξ_μέγεθος: %d\n", λεκ->λεξ_μέγεθος);

    /* ψηφιοποίησον σῶμα */
    int μῆκ_σώμ = (int)strlen(σῶμα);
    int *σημ_σώμ = malloc((size_t)(μῆκ_σώμ + 4) * sizeof(int));
    if (!σημ_σώμ) {
        free(σῶμα);
        ομφ_λεκτήρ_κατάλυσον(λεκ);
        return 1;
    }
    int ν_σημ_σώμ = 0;
    ομφ_λεκτήρ_τέμε(λεκ, σῶμα, 0, 0, σημ_σώμ, &ν_σημ_σώμ);
    printf("   σημεῖα ἐν σώματι: %d\n\n", ν_σημ_σώμ);
    free(σῶμα);

    /* σῶσον λεκτῆρα */
    if (ομφ_λεκτήρ_σῶσον(λεκ, "ὀμφαλός_dieta_1526.lex") < 0)
        fprintf(stderr, "προειδοποίησις: λεκτὴρ σωθῆναι οὐκ ἠδυνήθη\n");
    else
        printf("   λεκτὴρ ἐσώθη: ὀμφαλός_dieta_1526.lex\n\n");

    /* --- 3. ἄρχε ὑπόδειγμα --- */
    printf(
        "3. ἄρχω ὑπόδειγμα (διαστ=64, στρώμ=4, κεφ=4, λεξ=%d)...\n",
        λεκ->λεξ_μέγεθος
    );
    ομφ_σύνθεσις_t σύνθ = {
        .διάστασις    = 64,
        .διάστ_κρυ    = 172,
        .στρώματα     = 4,
        .κεφαλαί      = 4,
        .κεφαλαί_κτ   = 4,
        .λεξ_μέγεθος  = λεκ->λεξ_μέγεθος,
        .μῆκος_μέγ    = 64,
    };
    ομφ_t ομφ;
    if (ομφ_ἄρχε_τυχαίως(&ομφ, &σύνθ, 42u) < 0) {
        fprintf(stderr, "ομφ_ἄρχε_τυχαίως ἀπέτυχε\n");
        free(σημ_σώμ);
        ομφ_λεκτήρ_κατάλυσον(λεκ);
        return 1;
    }
    size_t ν_παρ = ομφ_μέγεθος_σταθμῶν(&σύνθ, 1);
    printf(
        "   παράμετροι: %zu δεκαδικά = %.1f KB\n\n",
        ν_παρ, ν_παρ * sizeof(float) / 1024.0
    );

    /* GPU:
        πέμψον σταθμὰ καὶ ἐνεργοποιήσεις εἰς GPU (εἰ ὑπάρχει καὶ ὠφελεῖ) */
    if (gpu == 0 && σύνθ.διάστασις >= 256) {
        if (ομφ_gpu_ἄρχε(&ομφ) == 0) {
            printf("   GPU ταμιεῖα ἐκχωρήθησαν\n\n");
        } else {
            printf("   GPU ταμιεῖα ἀπέτυχον — CPU χρῆται\n\n");
        }
    } else if (gpu == 0) {
        printf(
            "   διαστ=%d < 256: GPU οὐκ ὠφελεῖ, CPU χρῆται\n\n",
            σύνθ.διάστασις
        );
    }

    /* --- 4. ἄσκησον --- */
    printf("4. ἀσκῶ (%d βήματα)...\n", Ν_ΒΗΜΑΤΑ);
    ομφ_ἄσκησις_t ἀσκ;
    if (ομφ_ἄσκησις_ἄρχε(&ἀσκ, &σύνθ) < 0) {
        fprintf(stderr, "ομφ_ἄσκησις_ἄρχε ἀπέτυχε\n");
        ομφ_τέλει(&ομφ);
        free(σημ_σώμ);
        ομφ_λεκτήρ_κατάλυσον(λεκ);
        return 1;
    }
    ἄσκησον(&ομφ, &ἀσκ, σημ_σώμ, ν_σημ_σώμ, Ν_ΒΗΜΑΤΑ);
    ομφ_ἄσκησις_τέλει(&ἀσκ);
    free(σημ_σώμ);
    printf("\n");

    /* --- 5. σῶσον --- */
    printf("5. σῴζω ὑπόδειγμα...\n");
    if (ομφ_σῶσον(&ομφ, "ὀμφαλός_dieta_1526.bin") < 0)
        fprintf(stderr, "προειδοποίησις: ομφ_σῶσον ἀπέτυχε\n");
    else
        printf("   ὑπόδειγμα ἐσώθη: ὀμφαλός_dieta_1526.bin\n\n");
    ομφ_gpu_τέλει();
    ομφ_τέλει(&ομφ);

    /* --- 6. ἀνάγνωθι ὑπόδειγμα νέον --- */
    printf("6. ἀναγιγνώσκω ὑπόδειγμα ἐκ πτυχῆς...\n");
    ομφ_t ομφ2;
    if (ομφ_ἀνάγνωθι(&ομφ2, "ὀμφαλός_dieta_1526.bin") < 0) {
        fprintf(stderr, "ομφ_ἀνάγνωθι ἀπέτυχε\n");
        ομφ_λεκτήρ_κατάλυσον(λεκ);
        pfr_computo_fini();
        return 1;
    }
    printf(
        "   ὑπόδειγμα ἀνεγνώσθη: διαστ=%d στρώμ=%d λεξ=%d\n",
        ομφ2.σύνθεσις.διάστασις, ομφ2.σύνθεσις.στρώματα,
        ομφ2.σύνθεσις.λεξ_μέγεθος
    );
    if (gpu == 0 && ομφ2.σύνθεσις.διάστασις >= 256)
        ομφ_gpu_ἄρχε(&ομφ2);
    printf("\n");

    /* --- 7. συλλογισμοί --- */
    printf("7. γεννῶ κείμενον...\n");
    printf("   [προτροπή: \"Carolus Imperator\"]\n   ");
    γέννησον_καὶ_τύπωσον(&ομφ2, λεκ, "Carolus Imperator", 80);
    printf("\n");
    printf("   [προτροπή: \"Diomedes\"]\n   ");
    γέννησον_καὶ_τύπωσον(&ομφ2, λεκ, "Diomedes", 80);
    printf("\n");
    printf("   [προτροπή: \"avis\"]\n   ");
    γέννησον_καὶ_τύπωσον(&ομφ2, λεκ, "avis", 80);
    printf("\n");

    /* --- 8. δοκίμασον ἄμεσον γέννησιν ἐκ νέου λεκτῆρος --- */
    printf("8. δοκιμάζω ἄμεσον γέννησιν ἐκ νέου λεκτῆρος...\n");
    {
        ομφ_λεκτήρ_t *λεκ2 = ομφ_λεκτήρ_κτίσον(ΟΜΦ_ΛΕΞ_ΛΕΞΙΚΟΝ_ΠΡΟΕΠΙΛ);
        if (λεκ2 && ομφ_λεκτήρ_ἀνάγνωθι(λεκ2, "ὀμφαλός_dieta_1526.lex") == 0) {
            printf("   [προτροπή: \"bestia\"]\n   ");
            γέννησον_καὶ_τύπωσον(&ομφ2, λεκ2, "bestia", 64);
            ομφ_λεκτήρ_κατάλυσον(λεκ2);
        } else {
            printf("   νέος λεκτὴρ ἀναγνωσθῆναι οὐκ ἠδυνήθη\n");
            if (λεκ2)
                ομφ_λεκτήρ_κατάλυσον(λεκ2);
        }
    }
    printf("\n");

    ομφ_gpu_τέλει();
    ομφ_τέλει(&ομφ2);
    ομφ_λεκτήρ_κατάλυσον(λεκ);
    pfr_computo_fini();

    printf("=== δοκιμὴ τετελεσμένη ===\n");
    return 0;
}
