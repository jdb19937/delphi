/*
 * ὀμφαλός_λεκτήρ.c — λεκτὴρ BPE τοῦ ὀμφαλοῦ
 *
 * Byte-Pair Encoding: ἄρχεται ἐκ 256 σημείων ὀκτετικῶν,
 * συγχωνεύει ζεύγη συχνότατα ἕως οὗ λεξ_μέγ ἐπιτευχθῇ.
 */

#include "λεκτήρ.h"

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

/* ================================================================
 * ἐσωτερικὰ βοηθήματα
 * ================================================================ */

/*
 * ζήτησον_λέξημα — ζητεῖ νῆμα ἐν τεταγμένῳ λεξικῷ.
 * ἀποδίδει δείκτην εἰς λέξεις[] ἢ -1.
 */
static int ζήτησον_λέξημα(const ομφ_λεκτήρ_t *λεκ, const char *νῆμα)
{
    int κάτ = 0, ἄνω = λεκ->λεξ_μέγεθος - 1;
    while (κάτ <= ἄνω) {
        int μέσ = (κάτ + ἄνω) / 2;
        int σύγ = strcmp(λεκ->λέξεις[λεκ->τάξις[μέσ]].νῆμα, νῆμα);
        if (σύγ == 0)
            return λεκ->τάξις[μέσ];
        if (σύγ < 0)
            κάτ = μέσ + 1;
        else
            ἄνω = μέσ - 1;
    }
    return -1;
}

/*
 * σύγκρινον_λέξεις — συγκριτὴς πρὸς qsort (κατὰ νῆμα).
 */
static ομφ_λεκτήρ_t *γ_λεκ_τάξεως; /* ὁλικὸν βοήθημα πρὸς qsort */

static int σύγκρινον_λέξεις(const void *α, const void *β)
{
    int δα = *(const int *)α, δβ = *(const int *)β;
    return strcmp(
        γ_λεκ_τάξεως->λέξεις[δα].νῆμα,
        γ_λεκ_τάξεως->λέξεις[δβ].νῆμα
    );
}

/*
 * οἰκοδόμησον_τάξιν — (ξανα)οἰκοδομεῖ τάξις[] μετὰ ἀλλαγὰς λέξεων[].
 */
static void οἰκοδόμησον_τάξιν(ομφ_λεκτήρ_t *λεκ)
{
    for (int i = 0; i < λεκ->λεξ_μέγεθος; i++)
        λεκ->τάξις[i]  = i;
    γ_λεκ_τάξεως = λεκ;
    qsort(λεκ->τάξις, λεκ->λεξ_μέγεθος, sizeof(int), σύγκρινον_λέξεις);
}

/* ================================================================
 * κτίσον / κατάλυσον
 * ================================================================ */

ομφ_λεκτήρ_t *ομφ_λεκτήρ_κτίσον(int λεξ_μέγ)
{
    ομφ_λεκτήρ_t *λεκ = calloc(1, sizeof(*λεκ));
    if (!λεκ)
        return NULL;
    λεκ->λεξ_μέγ = λεξ_μέγ > 0 ? λεξ_μέγ : ΟΜΦ_ΛΕΞ_ΛΕΞΙΚΟΝ_ΠΡΟΕΠΙΛ;
    λεκ->λέξεις  = calloc(λεκ->λεξ_μέγ, sizeof(ομφ_λέξημα_t));
    λεκ->τάξις    = calloc(λεκ->λεξ_μέγ, sizeof(int));
    if (!λεκ->λέξεις || !λεκ->τάξις) {
        free(λεκ->λέξεις);
        free(λεκ->τάξις);
        free(λεκ);
        return NULL;
    }
    λεκ->λεξ_μέγεθος = 0;
    return λεκ;
}

void ομφ_λεκτήρ_κατάλυσον(ομφ_λεκτήρ_t *λεκ)
{
    if (!λεκ)
        return;
    free(λεκ->λέξεις);
    free(λεκ->τάξις);
    free(λεκ);
}

/* ================================================================
 * ἄσκησις BPE
 * ================================================================ */

int ομφ_λεκτήρ_ἄσκησον(ομφ_λεκτήρ_t *λεκ, const char *σῶμα)
{
    if (!λεκ || !σῶμα)
        return -1;

    /* --- ἀρχή: 256 σημεῖα δι᾽ ἕκαστον ὀκτέτον --- */
    for (int i = 0; i < 256; i++) {
        λεκ->λέξεις[i].νῆμα[0] = (char)i;
        λεκ->λέξεις[i].νῆμα[1] = '\0';
        λεκ->λέξεις[i].μῆκος  = 1;
        λεκ->λέξεις[i].βαθμός = 0.0f;
    }
    λεκ->λεξ_μέγεθος = 256;
    οἰκοδόμησον_τάξιν(λεκ);

    /* --- σῶμα εἰς ἀρχικὰ σημεῖα --- */
    int ν      = (int)strlen(σῶμα);
    int *σημεῖα = malloc((size_t)ν * sizeof(int));
    if (!σημεῖα)
        return -1;
    for (int i = 0; i < ν; i++)
        σημεῖα[i] = (unsigned char)σῶμα[i];

    /* ταμιεῖον συχνοτήτων (ζεῦγος = δύο συνεχῆ σημεῖα) */
    int ΛΜ      = λεκ->λεξ_μέγ;
    int *συχν = calloc((size_t)ΛΜ * ΛΜ, sizeof(int));
    if (!συχν) {
        free(σημεῖα);
        return -1;
    }

    /* --- βρόχος συγχωνεύσεως BPE --- */
    while (λεκ->λεξ_μέγεθος < λεκ->λεξ_μέγ) {
        /* μηδένισον πάσας συχνότητας */
        memset(συχν, 0, (size_t)ΛΜ * ΛΜ * sizeof(int));

        /* ἀρίθμησις ζευγῶν */
        for (int i = 0; i < ν - 1; i++) {
            if (σημεῖα[i] < 0 || σημεῖα[i+1] < 0)
                continue;
            συχν[(size_t)σημεῖα[i] * ΛΜ + σημεῖα[i+1]]++;
        }

        /* εὑρὲ ζεῦγος συχνότατον */
        int ἄρ_α = -1, ἄρ_β = -1, ἄρ_συχν = 0;
        for (int α = 0; α < λεκ->λεξ_μέγεθος; α++)
            for (int β = 0; β < λεκ->λεξ_μέγεθος; β++) {
            int σ = συχν[(size_t)α * ΛΜ + β];
            if (σ > ἄρ_συχν) {
                ἄρ_συχν = σ;
                ἄρ_α       = α;
                ἄρ_β       = β;
            }
        }

        if (ἄρ_συχν <= 1)
            break; /* οὐδὲν συγχωνεύεται */

        /* κτίσον λέξημα συγχωνεύσεως */
        int νέον = λεκ->λεξ_μέγεθος;
        int μα     = λεκ->λέξεις[ἄρ_α].μῆκος;
        int μβ     = λεκ->λέξεις[ἄρ_β].μῆκος;
        if (μα + μβ >= ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ)
            break;
        memcpy(
            λεκ->λέξεις[νέον].νῆμα,
            λεκ->λέξεις[ἄρ_α].νῆμα, μα
        );
        memcpy(
            λεκ->λέξεις[νέον].νῆμα + μα,
            λεκ->λέξεις[ἄρ_β].νῆμα, μβ + 1
        );
        λεκ->λέξεις[νέον].μῆκος  = μα + μβ;
        λεκ->λέξεις[νέον].βαθμός = (float)ἄρ_συχν;
        λεκ->λεξ_μέγεθος++;

        /* ἐνημέρωσον μέγ_λεξ_μῆκ */
        if (μα + μβ > λεκ->μέγ_λεξ_μῆκ)
            λεκ->μέγ_λεξ_μῆκ = μα + μβ;

        /* ἐφάρμοσον συγχώνευσιν εἰς σημεῖα */
        int j = 0;
        for (int i = 0; i < ν; ) {
            if (i < ν - 1 && σημεῖα[i] == ἄρ_α && σημεῖα[i+1] == ἄρ_β) {
                σημεῖα[j++] = νέον;
                i += 2;
            } else {
                σημεῖα[j++] = σημεῖα[i++];
            }
        }
        ν = j;

        /* (ξανα)οἰκοδόμησον τάξιν μετὰ προσθήκην */
        οἰκοδόμησον_τάξιν(λεκ);
    }

    free(συχν);
    free(σημεῖα);
    return 0;
}

/* ================================================================
 * σῶσον / ἀνάγνωθι
 * ================================================================ */

int ομφ_λεκτήρ_σῶσον(const ομφ_λεκτήρ_t *λεκ, const char *ὁδός)
{
    FILE *π = fopen(ὁδός, "wb");
    if (!π)
        return -1;

    unsigned int μαγικόν = ΟΜΦ_ΛΕΞ_ΣΗΜΕΙΟΝ_ΜΑΓΙΚΟΝ;
    fwrite(&μαγικόν,           sizeof(unsigned int), 1, π);
    fwrite(&λεκ->λεξ_μέγεθος, sizeof(int),          1, π);
    fwrite(&λεκ->μέγ_λεξ_μῆκ, sizeof(int),          1, π);

    for (int i = 0; i < λεκ->λεξ_μέγεθος; i++) {
        fwrite(&λεκ->λέξεις[i].βαθμός, sizeof(float), 1, π);
        fwrite(&λεκ->λέξεις[i].μῆκος,  sizeof(int),   1, π);
        fwrite(
            λεκ->λέξεις[i].νῆμα,     1,
            (size_t)λεκ->λέξεις[i].μῆκος + 1, π
        );
    }

    fclose(π);
    return 0;
}

int ομφ_λεκτήρ_ἀνάγνωθι(ομφ_λεκτήρ_t *λεκ, const char *ὁδός)
{
    FILE *π = fopen(ὁδός, "rb");
    if (!π)
        return -1;

    unsigned int μαγικόν;
    if (
        fread(&μαγικόν, sizeof(unsigned int), 1, π) != 1 ||
        μαγικόν != ΟΜΦ_ΛΕΞ_ΣΗΜΕΙΟΝ_ΜΑΓΙΚΟΝ
    ) {
        fclose(π);
        return -1;
    }

    int λμ, μλμ;
    if (
        fread(&λμ,  sizeof(int), 1, π) != 1 ||
        fread(&μλμ, sizeof(int), 1, π) != 1 ||
        λμ > λεκ->λεξ_μέγ
    ) {
        fclose(π);
        return -1;
    }
    λεκ->λεξ_μέγεθος = λμ;
    λεκ->μέγ_λεξ_μῆκ = μλμ;

    for (int i = 0; i < λμ; i++) {
        float βαθμός;
        int μῆκ;
        if (
            fread(&βαθμός, sizeof(float), 1, π) != 1 ||
            fread(&μῆκ,    sizeof(int),   1, π) != 1 ||
            μῆκ <= 0 || μῆκ >= ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ
        ) {
            fclose(π);
            return -1;
        }
        λεκ->λέξεις[i].βαθμός = βαθμός;
        λεκ->λέξεις[i].μῆκος  = μῆκ;
        if (fread(λεκ->λέξεις[i].νῆμα, 1, (size_t)μῆκ + 1, π) != (size_t)μῆκ + 1) {
            fclose(π);
            return -1;
        }
    }

    fclose(π);
    οἰκοδόμησον_τάξιν(λεκ);
    return 0;
}

/* ================================================================
 * τέμε (encode)
 * ================================================================ */

void ομφ_λεκτήρ_τέμε(
    const ομφ_λεκτήρ_t *λεκ, const char *κείμενον,
    int ἀρχ, int τελ, int *σημεῖα, int *ν_σημ
) {
    int ν = 0;

    if (ἀρχ)
        σημεῖα[ν++] = 1; /* σημεῖον ἀρχῆς (σύμβασις: δείκτης 1) */

    /* ἀρχή: σημεῖα δι᾽ ἕκαστον ὀκτέτον */
    for (const unsigned char *δ = (const unsigned char *)κείμενον; *δ; δ++)
        σημεῖα[ν++] = (int)*δ;

    /* BPE: συγχώνευσον ζεύγη ἕως δυνατόν */
    char ζεῦγος[ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ * 2];
    int μεταβληθέν = 1;
    while (μεταβληθέν) {
        μεταβληθέν = 0;
        float ἄριστος_βαθμός = -1e10f;
        int   ἄριστος_δείκτ  = -1;
        int   ἄριστος_ταυτ   = -1;

        for (int i = 0; i < ν - 1; i++) {
            /* κατασκεύασον νῆμα ζεύγους */
            int μα = λεκ->λέξεις[σημεῖα[i]].μῆκος;
            int μβ = λεκ->λέξεις[σημεῖα[i+1]].μῆκος;
            if (μα + μβ >= ΟΜΦ_ΛΕΞ_ΛΕΞΗΜΑ_ΜΕΓ)
                continue;
            memcpy(ζεῦγος,      λεκ->λέξεις[σημεῖα[i]].νῆμα,   μα);
            memcpy(ζεῦγος + μα, λεκ->λέξεις[σημεῖα[i+1]].νῆμα, μβ + 1);

            int ταυτ = ζήτησον_λέξημα(λεκ, ζεῦγος);
            if (ταυτ >= 0 && λεκ->λέξεις[ταυτ].βαθμός > ἄριστος_βαθμός) {
                ἄριστος_βαθμός = λεκ->λέξεις[ταυτ].βαθμός;
                ἄριστος_δείκτ   = i;
                ἄριστος_ταυτ     = ταυτ;
            }
        }

        if (ἄριστος_δείκτ < 0)
            break;

        /* ἐφάρμοσον συγχώνευσιν */
        σημεῖα[ἄριστος_δείκτ] = ἄριστος_ταυτ;
        for (int i = ἄριστος_δείκτ + 1; i < ν - 1; i++)
            σημεῖα[i] = σημεῖα[i + 1];
        ν--;
        μεταβληθέν = 1;
    }

    if (τελ)
        σημεῖα[ν++] = 2; /* σημεῖον τέλους (σύμβασις: δείκτης 2) */
    *ν_σημ = ν;
}

/* ================================================================
 * ἀπόδος (decode)
 * ================================================================ */

const char *ομφ_λεκτήρ_ἀπόδος(const ομφ_λεκτήρ_t *λεκ, int σημεῖον)
{
    if (σημεῖον < 0 || σημεῖον >= λεκ->λεξ_μέγεθος)
        return "?";
    return λεκ->λέξεις[σημεῖον].νῆμα;
}
